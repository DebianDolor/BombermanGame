package Commons;

public class Rect {

    public float x;
    public float y;
    public float w;
    public float h;

    public Rect() {
        x = 0.0f;
        y = 0.0f;
        w = 0.0f;
        h = 0.0f;
    }

    public Rect(float x, float y, float w, float h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
